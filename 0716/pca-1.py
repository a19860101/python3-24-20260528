import pandas as pd
import matplotlib.pyplot as plt

from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA
from sklearn.cluster import AgglomerativeClustering


# ==================================================
# 1. 建立資料
# ==================================================

data = {
    "顧客編號": [
        "C001", "C002", "C003", "C004", "C005",
        "C006", "C007", "C008", "C009", "C010",
        "C011", "C012", "C013", "C014", "C015",
        "C016", "C017", "C018", "C019", "C020",
        "C021", "C022", "C023", "C024", "C025",
        "C026", "C027", "C028", "C029", "C030"
    ],

    # 每月到店消費次數
    "每月到店次數": [
        2, 4, 3, 5, 2,
        6, 4, 3, 7, 5,
        9, 11, 8, 12, 10,
        14, 9, 13, 11, 15,
        18, 22, 20, 25, 17,
        24, 19, 27, 21, 23
    ],

    # 每次平均消費金額，單位為元
    "平均消費金額": [
        85, 120, 95, 150, 110,
        180, 135, 160, 210, 175,
        250, 320, 230, 350, 280,
        420, 300, 380, 340, 450,
        520, 680, 590, 750, 480,
        720, 550, 820, 650, 700
    ],

    # 每次平均停留時間，單位為分鐘
    "平均停留時間": [
        12, 18, 15, 25, 10,
        30, 22, 35, 28, 20,
        45, 60, 38, 75, 50,
        90, 55, 80, 65, 100,
        120, 150, 135, 180, 110,
        165, 125, 200, 145, 170
    ],

    # 每月使用優惠券次數
    "優惠券使用次數": [
        0, 1, 0, 2, 1,
        2, 1, 1, 3, 2,
        3, 4, 2, 5, 3,
        5, 3, 4, 4, 6,
        7, 9, 8, 10, 6,
        9, 7, 11, 8, 10
    ]
}

df = pd.DataFrame(data)
# ==================================================
# 1. 選擇要進行分群的特徵
# ==================================================

feature_columns = [
    "每月到店次數",
    "平均消費金額",
    "平均停留時間",
    "優惠券使用次數"
]

X = df[feature_columns]


# ==================================================
# 2. 標準化
# ==================================================

scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)


# ==================================================
# 3. 階層式分群
# ==================================================

model = AgglomerativeClustering(
    n_clusters=3,
    linkage="ward"
)

df["群組"] = model.fit_predict(X_scaled)


# ==================================================
# 4. 使用 PCA 將四維降成二維
# ==================================================

pca = PCA(n_components=2)

X_pca = pca.fit_transform(X_scaled)


# ==================================================
# 5. 將 PCA 結果放回 DataFrame
# ==================================================

df["主成分1"] = X_pca[:, 0]
df["主成分2"] = X_pca[:, 1]


# ==================================================
# 6. 顯示部分結果
# ==================================================

print(
    df[
        [
            "顧客編號",
            "主成分1",
            "主成分2",
            "群組"
        ]
    ]
)


# ==================================================
# 7. 畫出降維後的分群結果
# ==================================================

plt.figure(figsize=(9, 6))

scatter = plt.scatter(
    df["主成分1"],
    df["主成分2"],
    c=df["群組"],
    s=80
)

# 顯示顧客編號
for i in range(len(df)):
    plt.text(
        df.loc[i, "主成分1"] + 0.05,
        df.loc[i, "主成分2"] + 0.05,
        df.loc[i, "顧客編號"],
        fontsize=8
    )

plt.xlabel("Principal Component 1")
plt.ylabel("Principal Component 2")
plt.title("PCA Customer Clustering")

plt.colorbar(scatter, label="Cluster")

plt.grid()
plt.show()

print(pca.explained_variance_ratio_)