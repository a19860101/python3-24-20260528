import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.preprocessing import StandardScaler
from scipy.cluster.hierarchy import linkage, dendrogram


# ==================================================
# 1. 建立資料
# ==================================================

data = {
    "姓名": [
        "學生01", "學生02", "學生03", "學生04", "學生05",
        "學生06", "學生07", "學生08", "學生09", "學生10",
        "學生11", "學生12", "學生13", "學生14", "學生15",
        "學生16", "學生17", "學生18", "學生19", "學生20",
        "學生21", "學生22", "學生23", "學生24", "學生25",
        "學生26", "學生27", "學生28", "學生29", "學生30"
    ],

    # 特徵 1：每週運動時間，單位為小時
    "運動時間": [
        1.0, 1.5, 2.0, 2.5, 3.0,
        1.2, 2.2, 2.8, 3.5, 1.8,
        4.5, 5.0, 5.5, 6.0, 6.5,
        4.8, 5.2, 6.2, 5.8, 6.8,
        8.0, 8.5, 9.0, 9.5, 10.0,
        8.2, 9.2, 10.5, 11.0, 12.0
    ],

    # 特徵 2：每月消費金額，單位為元
    "消費金額": [
        5000, 6500, 7200, 8500, 9000,
        5500, 7800, 8200, 9500, 7000,
        18000, 20000, 22000, 25000, 27000,
        19000, 23000, 26000, 24000, 28000,
        42000, 45000, 48000, 50000, 52000,
        44000, 49000, 55000, 58000, 60000
    ],

    # 特徵 3：每週外出次數
    "外出次數": [
        1, 2, 2, 3, 3,
        1, 2, 3, 4, 2,
        5, 5, 6, 6, 7,
        5, 6, 7, 6, 7,
        9, 10, 10, 11, 12,
        9, 11, 12, 13, 15
    ]
}

df = pd.DataFrame(data)

print("原始資料：")
print(df)


# ==================================================
# 2. 取出要分群的特徵
# ==================================================

X = df[
    [
        "運動時間",
        "消費金額",
        "外出次數"
    ]
]


# ==================================================
# 3. 標準化
# ==================================================

scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)

# 將標準化後的資料轉成 DataFrame
scaled_df = pd.DataFrame(
    X_scaled,
    columns=[
        "運動時間_標準化",
        "消費金額_標準化",
        "外出次數_標準化"
    ]
)

scaled_df.insert(
    0,
    "姓名",
    df["姓名"]
)

print("\n標準化後資料：")
print(scaled_df.round(2))


# ==================================================
# 4. 四種連結方法
# ==================================================

methods = [
    "single",
    "complete",
    "average",
    "ward"
]


# ==================================================
# 5. 分別畫出四張樹狀圖
# ==================================================

for method in methods:

    # 建立階層式分群結果
    result = linkage(
        X_scaled,
        method=method
    )

    # 印出每一次合併距離
    distances = result[:, 2]

    print("\n" + "=" * 50)
    print(f"{method.upper()} Linkage")
    print("=" * 50)

    print("每一次合併距離：")
    print(np.round(distances, 2))

    # 每一種方法各畫一張樹狀圖
    plt.figure(figsize=(14, 6))

    dendrogram(
        result,
        labels=df["姓名"].tolist(),
        leaf_rotation=90,
        leaf_font_size=9
    )

    plt.title(
        f"Hierarchical Clustering - {method.capitalize()} Linkage"
    )

    plt.xlabel("Student")
    plt.ylabel("Distance")

    plt.tight_layout()
    plt.show()